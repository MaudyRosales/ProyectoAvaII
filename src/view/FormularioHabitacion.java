    public FormularioHabitacion(ListadoHabitaciones parent, boolean modal, String Mode, int id_huesped) {
        super(parent, modal);
        this.parent = parent;
        initComponents();
        setLocationRelativeTo(null);
    
        this.Mode = Mode;
        this.id_huesped = id_huesped;
        huesped = new Habitacion();        
    
        if (Mode.equals("UPD") || Mode.equals("DLT")) {
            huesped = (Habitacion) dao.getById(id_huesped);  // Obtener el huésped por id
            txtID.setText(String.valueOf(huesped.getId_habitacion()));
            txtNombre.setText(huesped.getTipo());
            txtEmail.setText(String.valueOf(huesped.getPrecio()));
            txtTelefono.setText(String.valueOf(huesped.getCapacidad()));
            txtFecha.setText(huesped.getEstado());
        }      
    
        if (Mode.equals("INS")) {
            btnOperacion.setText("Insertar");
        } else if (Mode.equals("UPD")) {
            btnOperacion.setText("Modificar");
        } else if (Mode.equals("DLT")) {
            btnOperacion.setText("Borrar");
        }
    }

    
    private boolean validar(){
        if(txtNombre.getText().equals("") || txtNombre.getText().equals(null)){
            JOptionPane.showMessageDialog(parent, "Debe ingresar un nombre", "ERROR DE VALIDACIÓN", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

