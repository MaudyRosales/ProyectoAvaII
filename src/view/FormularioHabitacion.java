    private void btnOperacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOperacionActionPerformed
    if (Mode.equals("INS")) {
        boolean validacion = validar();
        if (validacion) {
            Habitacion newHuesped = new Habitacion();
            newHuesped.setTipo(txtNombre.getText());
            newHuesped.setPrecio(Double.parseDouble(txtEmail.getText()));
            newHuesped.setCapacidad(Integer.parseInt(txtTelefono.getText()));
            newHuesped.setEstado(txtFecha.getText());
            
            boolean operacion = dao.insert(newHuesped);
            if (operacion) {
                parent.refrescarDatos();
                this.dispose();
            }
        }
    }

    if (Mode.equals("UPD")) {
        boolean validacion = validar();
        if (validacion) {
            huesped.setTipo(txtNombre.getText());
            huesped.setPrecio(Double.parseDouble(txtEmail.getText()));
            huesped.setCapacidad(Integer.parseInt(txtTelefono.getText()));
            huesped.setEstado(txtFecha.getText());
            // Actualizar el huesped
            boolean operacion = dao.update(huesped);
            if (operacion) {
                parent.refrescarDatos();
                this.dispose();
            }
        }
    }
    }//GEN-LAST:event_btnOperacionActionPerformed

